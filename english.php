<h3>ACTIVITIES</h3>
<b>Trips to the NTNUI "koier":</b><br>
"Koiene" are cottages owned by NTNUI, and trips here are an important part of the swimming team. The swimming team goes on weekend trips to one of the cottages twice a year to enjoy beautiful nature, outdoor life (including skiing), tasty food and social fun. Some of the cottages also have saunas ready for us to enjoy. Youll never regret having participated on one of these trips!
<br><br>
<b>Students championships:</b><br>
Once a year, a competition is held by the different students sports teams in Norway where swimming teams from the student groups meet to compete against each other. One of the highlights is the beer relay, where NTNUI historically has shown that theyre unbeatable! Afterwards, there is a banquet filled with laughter, fun and dancing.
<br><br>
<b>Klubbkampen:</b><br>
Once a year, NTNUI meets the students from Oslo (OSI) to compete for a traveling trophy. We switch turns in arranging the swim meet. This competition is also followed by a banquet.
<br><br>
<b>NTNUI-Sprinten:</b><br>
NTNUI-Sprinten is a competition arranged entirely by NTNUI, and will take place for the first time on March 18th, 2007. <a href="index.php?side=sprinten.php">More information.</a>
<br><br>
<b>Social stuff:</b><br>
The swimmers of NTNUI are a bunch of social creatures who enjoy meeting up also outside of the swimming pool. The traditional beer on Fridays is an example of this. Join us whenever youd like! In order to get the latest info on social events, you would benefit greatly by <a href="http://redacted-domain.com/listinfo.cgi/svommer-sosial">registering</a> on the mailing list for social events.
<br>
Some of these activities have resulted in some entertaining pictures, that you can view on this page: http://www.redacted-domain.com/svomming/gallery/albums.php
