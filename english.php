<h3>Practise</h3>

<b>Practice:</b><br>
Mondays: 2100 - 2230<br>
Tuesdays: 2100 - 2230<br>
Wednesdays: 2030 - 2200<br>
Fridays: 2030 - 2200<br>
<br>
<b>About</b><br>
All of NTNUIs practice hours take place at Pirbadet.
The members of the swimming group are ONLY allowed to use the regular pool of 50 metres, thus the other facilities such as the water slide and the jacuzzi are off limits unless you purchase a normal ticket.
Also, members have to show up during the hours of training, because NTNUI only administer the pool for a limited amount of time<br>
<br>
<b>How many lanes and who swims where?</b><br>

Mondays: 8 lanes all through practice (Two 50m lanes)<br>
Tuesdays: 8 lanes all through practice<br>
Wednesdays: 8 lanes from 9 oclock<br>
Fridays: 6 lanes all through practice<br>
<br>
We emphasize that you have to be able to swim in order to show up at our practice. NTNUI contains of swimmers on all levels, and everyone has their own pace. What we try to achieve at practice is that swimmers of approximately the same level and pace swim together in the same lanes. Read more about the specific lanes further down.<br>
<br>

<b>Slow pace</b><br>
For swimmers who are at the beginners level or for those who just want to have a relaxing swim.<br>
Approximate amount: Under 2000 metres.<br>
Lanes 8 and 7.<br>
<br>
<b>Medium pace</b><br>
For swimmers with a bit of experience, but not necessarily competition experience. Some swim by program here, but usually a bit shorter than the fastest lane.<br>
Approximate amount: 2000 + meters.<br>
Lanes 6, 5 and 1 (when available).<br>
<br>
<b>Program lane/fast pace</b><br>
For swimmers who are still active or used to be quite active. Mostly people who used to compete a bit. Swimmers in these lanes swim by a program in two or three lanes depending on how many people attending practice on the particular day.<br>
Approximate amount: 3000 + metres.<br>
Lanes 4,3 and 2 (when available)<br>
