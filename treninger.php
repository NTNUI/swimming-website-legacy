<h2>Treninger</h2>

<b>Treningstider:</b>
<ul>
<li>Mandager 2100 - 2230</li>
<li>Tirsdager 2100 - 2230</li>
<li>Onsdager 2030 - 2200</li>
<li>Fredager 2030 - 2200</li>
</ul>
<b>Om treningene:</b><br>
Alle treningene til NTNUI-Svømmegruppa foregår i Pirbadet.<br>
<br>
Svømmegruppa har KUN leid plass i 50m bassenget
og det er derfor IKKE anledning for medlemmer av svømmegruppa
å benytte seg av Pirbadets andre fasiliteter, som for
eksempel boblebad, vannsklie og helsebad.<br>
<br>
Svømmegruppa har bare den tilmålte tiden, så
møt opp til treningen i tide!<br>
<br>

<b>Banefordeling:</b>
<ul>
<li>Mandag: 8 baner hele tiden (to langbaner)</li>
<li>Tirsdag: 8 baner hele treningen</li>
<li>Onsdag: 6 baner (8 baner fra kl 2100)</li>
<li>Fredag: 6 baner hele treningen</li>
</ul>

Noen baner er satt av til svømmere som ønsker å
svømme program, mens noen baner blir satt av til svømmere som
ønsker å ta det mer med ro og følge sitt eget opplegg.
Vi jobber med ett opplegg for inndeling av banene slik at de
som svømmer omtrent like fort kan svømme sammen.
I prinsippet fungerer det slik at de som vil ta det rolig svømmer nermest havet,
og så øker intensiteten gradvis innover i bassenget.
De to innerste banene (1 og 2) blir brukt av de "gruppene" som har mest plassmangel i banen sin når
vi har tilgang til disse banene.<br>
<br>
<b>Roligbane</b><br>
For deg som er ny med tanke på svømming som idrett, eller rett og slette bare ønsker å ta det rolig og slappe av i bassenget.<br>Treningsmengde: Under 2000m<br>Bane 8 og 7<br>
<br>
<b>Trimsvømming</b><br>
For deg som har svømt litt før men ikke nødvendigvis har vert med på konkurranser. Også noen av disse svømmer program, men noe kortere enn snøgbanene. Mange i denne kategorien ender opp i en av snøggbane når de kommer i bedre form.<br>Treningsmengde: 2000m - 3000m<br>Bane 6, 5 og 1 (Når tilgjengelig)<br>
<br>
<b>Racerbane</b><br>
Typisk for deg som er tidligere konkurransesvømmer, eller fortsatt aktiv. Disse banene svømmer program og deler seg selv inn i 2-3 baner avhengig av hvor mange som er på trening. Her har vi alt ifra norgesmestere til folk som la opp på videregående.<br>Treningsmengde: Over 3000m<br>Bane 4, 3 og 2 (Når tilgjengelig)<br>
