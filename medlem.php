<h2>Medlemskap</h2>

<b><b>For å bli medlem i svømmegruppa må du gjøre følgende:</b></b><br>
<br>
<b>1. Være svømmedyktig</b><br>
	Du trenger ikke være tidligere konkurransesvømmer.
	Normal norsk svømmeundervisning fra barneskolen er tilstrekkelig for at du skal kunne kose deg i bassenget.
	Det er mulig å hooke tak i noen av de raskere svømmerne og spør om noen tips dersom du føler for det,
	men du kan ikke forvente å få noen nybegynneropplæring.<br>
<br>
<b>2. Ha gyldig medlemskap i NTNUI</b><br>
	Dette kan kjøpes på Idrettsbyggene på Gløshaugen og Dragvoll.<br>
	NTNUI er en organisasjon som er bygget på dugnad og frivillighet. Vi forventer derfor at våre medlemmer stiller seg positive til å bidra til dette.
	Oppgaver som våre medlemmer må påregne å bli spurt om å delta på i løpet av året er vakt på idrettsbygget eller funksjonær på NTNUI-Sprinten.
	Se <a href="redacted-domain.com">REDACTED</a> for mer info.<br>
<br>
<b>3. Ha gyldig lisens til Norges Svømmeforbund</b><br>
	Lisens kr. 450 betales til Kontonummer 5361.06.92571 (53610692571)<br>
	Da sørger svømmegruppa for at lisenesen betales og du blir ført opp på medlemslista.<br>
	NB! Skriv navnet ditt på betalingen!
    
    <br>
    <br>
    Lisensen varer i ett kalenderår, fra januar til januar. De som betaler første gang om høsten må betale igjen i januar.
<br>
<br>

Pirbadet krever at alle våre medlemmer har lisens for at vi skal kunne leie der. Startlisensen gir utøverne rett til å delta på konkurranser, bladet Norsk Svømming og idrettsskadeforsikring. Det er Norges Svømmeforbund som bestemmer hva lisensen skal være på.
    
<br>
<br>
    
    
	(Dersom du har betalt lisens til annen klubb i løpet av de siste 2 åra må du melde overgang eller betale lisens til denne klubben og gi oss beskjed om det.)<br>
<br>
<b>4. Gi beskjed til kasserer</b><br>
	Dette kan gjøres ved å fylle ut følgende <a href="index.php?side=innmelding.php">skjema</a>.<br>
	Opplysningene blir brukt til å registrere lisensen din hos svømmeforbundet, samt å generere adgangslister til Pirbadet.<br>
	Opplysningene kan også bli brukt til å kontrollere at våre medlemmer har gyldig medlemskap i NTNUI. Dette skjemaet må også fylles ut på nytt hver gang du betaler lisens på nytt, altså i januar.<br>

    
<br>
<b>5. Sett deg på epostlista</b><br>
	For å få løpende informasjon bør du <a href="http://redacted-domain.com/listinfo.cgi/svommer-komplett">registrere deg</a> på e-post lista. e-post lista. Her får du nyttig informasjon og påminnelser om

lisensbetaling i januar, diverse info om treninger og sosiale happenings som koietur.<br>
<br>
<b>6. Du må ha adgangskort i pirbadet</b><br>
	6. Du må ha adgangskort i pirbadet
Du må vente på mail fra kasserer om at du er satt opp på listen i
Pirbadet. Når du har mottatt denne kan du hente adgangskort ved første
oppmøte i Pirbadet.
Du må da henvende deg i skranken FØR pirbadets ordinære stengetid (20:30).<br>
<br>
